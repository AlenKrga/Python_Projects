# create variables and assign values
john = 3
mary = 5
adam = 6

# print variables on one line
print(john, mary, adam, sep=",")

# calculate total apples
total_apples = john + mary + adam

# print total apples
print(total_apples)

# create new variables and perform arithmetic operations
extra_apples = 2
john += extra_apples
mary -= extra_apples
adam *= 2
total_apples = john + mary + adam

# print string and integer together
print("Total number of apples:", total_apples)
